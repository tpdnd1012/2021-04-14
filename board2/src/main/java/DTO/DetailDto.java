package DTO;

public class DetailDto {
	
	private int detail_id;
	private String detail_product_id;
	private String detail_orders_id;
	private int detail_amount;
	private int detail_state;
	
	
	public int getDetail_id() {
		return detail_id;
	}
	public void setDetail_id(int detail_id) {
		this.detail_id = detail_id;
	}
	public String getDetail_product_id() {
		return detail_product_id;
	}
	public void setDetail_product_id(String detail_product_id) {
		this.detail_product_id = detail_product_id;
	}
	public String getDetail_orders_id() {
		return detail_orders_id;
	}
	public void setDetail_orders_id(String detail_orders_id) {
		this.detail_orders_id = detail_orders_id;
	}
	public int getDetail_amount() {
		return detail_amount;
	}
	public void setDetail_amount(int detail_amount) {
		this.detail_amount = detail_amount;
	}
	public int getDetail_state() {
		return detail_state;
	}
	public void setDetail_state(int detail_state) {
		this.detail_state = detail_state;
	}
	
	
}
